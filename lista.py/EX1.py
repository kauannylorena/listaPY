# Definindo a mensagem a ser repetida
mensagem = "Eu estou aprendendo a escrever programas de computador! É muito empolgante!"

# Usando um laço de repetição 'for' para imprimir a mensagem 20 vezes
for i in range(20):
    print(mensagem)


#  números ímpares de 1 até 20
for i in range(1, 21, 2):
    print(i)
